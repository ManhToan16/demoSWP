
package toan;


 public class Car{
    private String maker;
    private int price;
    
    Car(String maker,int price){
        this.maker=maker;
        this.price=price;
        
    }

    public String getMaker() {
        return maker;
    }

    public void setMaker(String maker) {
        this.maker = maker;
    }

    public int getPrice() {
        return price;
    }

    
    
    
}


